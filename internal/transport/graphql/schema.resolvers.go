// InventoryLot returns InventoryLotResolver implementation.
func (r *Resolver) InventoryLot() InventoryLotResolver { return &inventoryLotResolver{r} }

// =========================================================================
// MUTATIONS
// =========================================================================

// CreateSalesOrder natively invokes the Temporal Saga execution boundary.
func (r *mutationResolver) CreateSalesOrder(ctx context.Context, input CreateOrderInput) (*revenue.SalesOrder, error) {
	// Structure the payload for the Pipeline Nervous System
	var pipelineItems []pipeline.OrderItem
	for _, item := range input.Items {
		pipelineItems = append(pipelineItems, pipeline.OrderItem{
			LotID:    item.LotID,
			Quantity: item.Quantity,
		})
	}

	req := pipeline.FulfillmentRequest{
		TransactionID:    input.TransactionID,
		CustomerID:       input.CustomerID,
		AccountDebitID:   input.AccountDebitID,
		AccountCreditID:  input.AccountCreditID,
		TotalAmountCents: int64(input.TotalAmountCents),
		Items:            pipelineItems,
	}

	// Asynchronously trigger the Saga via the pipeline
	if err := r.TemporalTrigger(ctx, req); err != nil {
		return nil, fmt.Errorf("failed to invoke saga orchestration: %w", err)
	}

	// Logically persist the initial domain truth before saga concludes async
	order := &revenue.SalesOrder{
		ID:         input.TransactionID,
		CustomerID: input.CustomerID,
		TotalValue: int64(input.TotalAmountCents),
		Status:     "PENDING_SAGA_COMPLETION",
		CreatedAt:  time.Now(),
		UpdatedAt:  time.Now(),
	}

	if err := r.RevenueService.InsertSalesOrder(ctx, order); err != nil {
		return nil, err
	}

	return order, nil
}

// =========================================================================
// QUERIES
