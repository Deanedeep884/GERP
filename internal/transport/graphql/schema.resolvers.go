// This file implements the graph mappings requested by the schema.

type Resolver struct {
	MDMService      mdm.Service
	EAMService      eam.Service
	RevenueService  revenue.Service
	TemporalTrigger func(ctx context.Context, req pipeline.FulfillmentRequest) error
}

type mutationResolver struct{ *Resolver }
