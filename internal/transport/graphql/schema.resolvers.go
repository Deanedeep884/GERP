func (r *queryResolver) GetSalesOrder(ctx context.Context, id uuid.UUID) (*revenue.SalesOrder, error) {
	return r.RevenueService.GetSalesOrder(ctx, id)
}

func (r *queryResolver) GetInventoryLot(ctx context.Context, id uuid.UUID) (*scm.InventoryLot, error) {
	// Dummy query resolution (Assumes SCM service exposes GetLot inherently)
	return nil, nil
}

// =========================================================================
// GRAPH STITCHING / GOLDEN THREAD RESOLVERS
// =========================================================================

// Customer crosses from Revenue over to MDM utilizing the CustomerID pointer.
func (r *salesOrderResolver) Customer(ctx context.Context, obj *revenue.SalesOrder) (*mdm.GlobalEntity, error) {
	// Resolves the Golden Record via the internal isolated domain logic
	entity, _, err := r.MDMService.GetGlobalEntityWithMappings(ctx, obj.CustomerID)
	return entity, err
}

// Warehouse deeply bridges SCM hardware components to EAM physical architecture.
func (r *inventoryLotResolver) Warehouse(ctx context.Context, obj *scm.InventoryLot) (*eam.Asset, error) {
	// Resolves the Physical Asset layout independently
	asset, _, err := r.EAMService.GetAssetWithLogs(ctx, obj.WarehouseID)
	return asset, err
}

